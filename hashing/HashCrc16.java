    public byte[] getCRC(byte[] buf, int len ) {

    	int crc =  0xFFFF;
    	byte[] crcCalc = new byte[2];

      for (int pos = 0; pos < len; pos++) {
        crc ^= (int)(0x00ff & buf[pos]);  // FIX HERE -- XOR byte into least sig. byte of crc

        for (int i = 8; i != 0; i--) {    // Loop over each bit
          if ((crc & 0x0001) != 0) {      // If the LSB is set
            crc >>= 1;                    // Shift right and XOR 0xA001
            crc ^= 0xA001;
          }
          else                            // Else LSB is not set
            crc >>= 1;                    // Just shift right
        }
      }
    // Note, crc has low and high bytes swapped, so use it accordingly (or swap bytes)
    crcCalc[0] =  (byte)(crc);//c6
    crcCalc[1] =  (byte)(crc >> 8);//b6
    return crcCalc;
}   // end GetCRC


